function normalizeCity(s: string): string {
  return s
    .toLowerCase()
    .trim()
    .replace(/[«»"`'.,]/g, '')
    .replace(/^г\.?\s+/i, '')        // «г. Москва» → «москва»
    .replace(/^город\s+/i, '')
    .replace(/ё/g, 'е')
    .replace(/\s+/g, ' ')
    .trim();
}

/** Найти geo_id по названию города. null если не нашли. */
export function findGeoId(city: string): number | null {
  if (!city) return null;
  const key = normalizeCity(city);
  return RUSSIA_GEO_IDS[key] ?? null;
}

/** Список уникальных городов для UI-селектора, отсортированный по алфавиту. */
export function listCities(): { name: string; geoId: number }[] {
  const seen = new Set<number>();
  const out: { name: string; geoId: number }[] = [];
  for (const [key, geoId] of Object.entries(RUSSIA_GEO_IDS)) {
    if (seen.has(geoId)) continue;
    seen.add(geoId);
    // Капитализируем первую букву и каждое слово после дефиса
    const label = key
      .split(/(?<=[\s-])/)
      .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
      .join('');
    out.push({ name: label, geoId });
  }
  out.sort((a, b) => a.name.localeCompare(b.name, 'ru'));
  return out;
}
