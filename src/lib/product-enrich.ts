// Обогащение описания товара дополнительным текстом для SEO.
// Берёт базовое описание (часто короткое из БД), добавляет:
//   - типизированный «features» абзац (из specs)
//   - «delivery» абзац с УТП магазина
//   - «warranty» абзац (для чемоданов) / уход (для кожи)
// Результат — длинное уникальное описание для каждой страницы товара,
// которое помогает в ранжировании по «{model} купить недорого Москва».

import type { ProductView } from '@/lib/products';

export interface EnrichedDescription {
  /** Основной текст из БД */
  base: string;
  /** Параграфы дополнительного контента */
  extra: string[];
}

const SUITCASE_BRANDS = ['VIP COLLECTION', 'ARISTOCRAT'];

export function enrichProductDescription(p: ProductView, categoryName: string | null): EnrichedDescription {
  const extra: string[] = [];
  const isSuitcase = p.categoryId === 'suitcases';
  const isLeather =
    p.categoryId === 'wallets' ||
    p.categoryId === 'briefcases' ||
    p.categoryId === 'belts' ||
    /натуральная кожа/i.test(String(p.specs?.['Материал'] ?? ''));

  // 1. Features paragraph — собираем из specs
  const specs = p.specs ?? {};
  const featureBits: string[] = [];
  if (specs['Размер']) featureBits.push(`Размер ${specs['Размер']}`);
  if (specs['Материал']) featureBits.push(`Материал: ${specs['Материал']}`);
  if (specs['Замок']) featureBits.push(`Замок ${specs['Замок']}`);
  if (specs['Ноутбук']) featureBits.push(`Подходит под ноутбук ${specs['Ноутбук']}`);
  if (specs['Комплект']) featureBits.push(`Комплект: ${specs['Комплект']}`);
  if (featureBits.length > 0) {
    extra.push(featureBits.join('. ') + '.');
  }

  // 2. Категорийный контекст
  if (isSuitcase) {
    extra.push(
      `Все чемоданы ${p.brand} — на 4 двойных колёсах с поворотом 360°, ` +
        'с алюминиевой телескопической ручкой и шёлковой подкладкой. ' +
        'Корпус из ударопрочного поликарбоната восстанавливает форму после ' +
        'удара и не царапается при перевозке в багажном отделении.'
    );
  } else if (p.categoryId === 'women-bags' && p.brand === 'David Jones') {
    extra.push(
      'Сумки David Jones изготовлены из качественной экокожи с реалистичной ' +
        'текстурой натуральной кожи. Аккуратная двойная прострочка, металлическая ' +
        'фурнитура, надёжные молнии YKK.'
    );
  } else if (isLeather && p.brand === 'NERI KARRA') {
    extra.push(
      'NERI KARRA — турецкий бренд кожгалантереи с 30-летней историей. Используется ' +
        'натуральная кожа КРС полного и среднего дубления. Со временем кожа приобретает ' +
        'благородную патину и становится только красивее.'
    );
  } else if (p.categoryId === 'backpacks' && p.brand === 'ARISTOCRAT') {
    extra.push(
      'Рюкзаки ARISTOCRAT — наш собственный бренд для города и работы. Водоотталкивающий ' +
        'нейлон 1680D, выделенный отсек для ноутбука с мягкими стенками, ортопедическая ' +
        'спинка с сетчатой панелью.'
    );
  } else if (p.categoryId === 'covers') {
    extra.push(
      'Эластичный чехол из спандекса плотно облегает корпус чемодана любого бренда. ' +
        'Защищает от царапин, грязи и потёртостей при перевозке в багажном отделении. ' +
        'Можно стирать в машинке при 40°C.'
    );
  } else if (p.categoryId === 'parts') {
    extra.push(
      'Универсальная запчасть для самостоятельной замены или сервисного ремонта. ' +
        'Подходит к большинству моделей VIP COLLECTION и ARISTOCRAT, многие детали ' +
        'совместимы также с Samsonite и American Tourister.'
    );
  }

  // 3. Доставка и оплата — единый блок
  extra.push(
    'Самовывоз бесплатно — Москва, Сормовский проезд, 11, стр. 1. ' +
      'Курьер по Москве — от 100 до 500 ₽ в зависимости от размера товара, ' +
      'при заказе от 20 000 ₽ — бесплатно в пределах МКАД. Доставка в Подмосковье — ' +
      'расчёт по адресу. Оплата картой онлайн, по СБП, картой или наличными при получении.'
  );

  // 4. Гарантия / уход
  if (SUITCASE_BRANDS.includes(p.brand) && (isSuitcase || p.categoryId === 'backpacks')) {
    extra.push(
      `На товары ${p.brand} действует гарантия производителя 12 месяцев. ` +
        'Гарантийный ремонт (замена колёс, ручек, замков, бегунков, молний) — ' +
        'бесплатно в собственном сервисном центре на Сормовском проезде, 11.'
    );
  } else if (isLeather) {
    extra.push(
      'Кожаное изделие при правильном уходе прослужит 7–10 лет. Периодически ' +
        'протирайте мягкой сухой тканью, раз в 2–3 месяца наносите кондиционер ' +
        'для кожи. Не оставляйте на солнце и под дождём.'
    );
  }

  // 5. Cross-link к категории
  if (categoryName) {
    extra.push(
      `Этот товар представлен в категории «${categoryName}» — в каталоге вы найдёте ` +
        'десятки моделей разных размеров, цветов и брендов.'
    );
  }

  return {
    base: p.description || `${p.name} — бренд ${p.brand}.`,
    extra,
  };
}
