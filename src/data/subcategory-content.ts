// Посадочные страницы /catalog/{categorySlug}/{variant} —
// отдельные лендинги под СЧ-запросы: цвета, размеры, типы, фичи.
// Цель: ранжироваться по «розовый чемодан Москва», «кейс-пилот купить»,
// «рюкзак для ноутбука 17 дюймов», «обложка для паспорта Москва» и т.п.
//
// Каждый вариант имеет:
//   - filter: предикат на ProductView для фильтрации товаров
//   - уникальный H1, intro, sections, FAQ (≥ 200 слов, иначе Google пометит как thin content)

import type { ProductView } from '@/lib/products';

export interface CategoryVariant {
  /** URL-сегмент: /catalog/{categorySlug}/{slug} */
  slug: string;
  metaTitle: string;
  metaDescription: string;
  keywords: string[];
  h1: string;
  tagline: string;
  intro: string;
  sections: { title: string; body: string }[];
  faq?: { q: string; a: string }[];
  /** Фильтр товаров (применяется к каталогу категории) */
  filter: (p: ProductView) => boolean;
}

/**
 * Хелперы для построения фильтров:
 * regex по названию, по spec, по бренду, по цене.
 */
const nameMatch = (re: RegExp) => (p: ProductView) => re.test(p.name);
const specMatch = (key: string, valueRe: RegExp) => (p: ProductView) =>
  valueRe.test(String(p.specs?.[key] ?? ''));
const brandIs = (b: string) => (p: ProductView) =>
  p.brand.toLowerCase() === b.toLowerCase();
const anyOf =
  (...fns: Array<(p: ProductView) => boolean>) =>
  (p: ProductView) => fns.some((f) => f(p));

