/** Все валидные пары (categorySlug, variantSlug) для sitemap и generateStaticParams */
export function getAllVariantPairs(): Array<{ categorySlug: string; variantSlug: string }> {
  // categorySlug в URL — это slug категории (chemodany / women-bags и т.д.).
  // На production все slug-и приведены к английским ID, поэтому slug = id.
  // (См. scripts/migrate-category-slugs.ts.)
  const pairs: Array<{ categorySlug: string; variantSlug: string }> = [];
  for (const [categoryId, variants] of Object.entries(categoryVariants)) {
    for (const v of variants) {
      pairs.push({ categorySlug: categoryId, variantSlug: v.slug });
    }
  }
  return pairs;
}

export function getVariant(categorySlug: string, variantSlug: string): CategoryVariant | undefined {
  return categoryVariants[categorySlug]?.find((v) => v.slug === variantSlug);
}
